#include <cstdint>
#include <string>

namespace libwire::ipv4 {
    struct address {
        uint8_t parts[4];

        inline std::string to_string() const {
            return std::to_string(parts[0]) + '.' +
                   std::to_string(parts[1]) + '.' +
                   std::to_string(parts[2]) + '.' +
                   std::to_string(parts[3]);
        }
    };
} // namespace libwire::ipv4

